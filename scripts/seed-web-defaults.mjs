// Bundles the built-in "default" scenario (server/data/scenarios/default) into a
// single JS module the WEB build imports so a fresh browser can seed its
// IndexedDB library with a playable scenario. Runs only from `npm run build:web`
// (and dev:web); the output dir is git-ignored and is never imported by the
// local build, so no seed data ships in the downloaded app.
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import path from "node:path";
import url from "node:url";

const ROOT = path.resolve(path.dirname(url.fileURLToPath(import.meta.url)), "..");
const SRC = path.join(ROOT, "server", "data", "scenarios", "default");
const OUT_DIR = path.join(ROOT, "src", "runtime", "web", "generated");
const OUT_FILE = path.join(OUT_DIR, "defaultScenario.js");
const COUNTRY_NAMES_OUT = path.join(OUT_DIR, "countryNames.js");
const FALLBACK_COLORS_OUT = path.join(OUT_DIR, "fallbackColors.js");

const readJson = (rel, fallback) => {
  const file = path.join(SRC, rel);
  if (!existsSync(file)) return fallback;
  try {
    return JSON.parse(readFileSync(file, "utf8"));
  } catch {
    return fallback;
  }
};

const meta = readJson("scenario.json", {});
const cover = (() => {
  const file = path.join(SRC, "cover-image.bin");
  if (!existsSync(file)) return null;
  const contentType = typeof meta.coverImageContentType === "string" && meta.coverImageContentType
    ? meta.coverImageContentType
    : "image/jpeg";
  return { contentType, base64: readFileSync(file).toString("base64") };
})();

const seed = {
  meta,
  cover,
  colors: readJson("colors.json", {}),
  data: {
    game: readJson("game.json", {}),
    prompts: readJson("prompts.json", {}),
    world: readJson("world.json", {}),
    actions: readJson("storage/actions.json", []),
    advisor: readJson("storage/advisor.json", []),
    chat: readJson("storage/chat.json", []),
    events: readJson("storage/events.json", []),
  },
};

// The canonical country registry (code -> name) used by the runtime write path's
// canonicalizeCountryRef, mirroring server/country-names.json.
const countryNames = (() => {
  const file = path.join(ROOT, "server", "country-names.json");
  if (!existsSync(file)) return {};
  try {
    return JSON.parse(readFileSync(file, "utf8"));
  } catch {
    return {};
  }
})();

mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(
  OUT_FILE,
  `/* AUTO-GENERATED by scripts/seed-web-defaults.mjs — do not edit. */\nexport default ${JSON.stringify(seed)};\n`,
  "utf8",
);
writeFileSync(
  COUNTRY_NAMES_OUT,
  `/* AUTO-GENERATED by scripts/seed-web-defaults.mjs — do not edit. */\nexport default ${JSON.stringify(countryNames)};\n`,
  "utf8",
);

// The app-level default palette the server serves as the colors fallback for a
// scenario that ships no colors of its own (server COLORS_ASSET_CANDIDATES =
// public/assets/colors.json). Bundled separately so the fallback is immutable
// and scenario-independent, matching the server.
const fallbackColors = (() => {
  const file = path.join(ROOT, "public", "assets", "colors.json");
  if (!existsSync(file)) return {};
  try {
    return JSON.parse(readFileSync(file, "utf8"));
  } catch {
    return {};
  }
})();
writeFileSync(
  FALLBACK_COLORS_OUT,
  `/* AUTO-GENERATED by scripts/seed-web-defaults.mjs — do not edit. */\nexport default ${JSON.stringify(fallbackColors)};\n`,
  "utf8",
);

const kb = Math.round(Buffer.byteLength(JSON.stringify(seed)) / 1024);
console.log(`web seed: default scenario → ${path.relative(ROOT, OUT_FILE)} (${kb} KB${cover ? ", cover embedded" : ""}); country names → ${path.relative(ROOT, COUNTRY_NAMES_OUT)} (${Object.keys(countryNames).length} entries)`);
